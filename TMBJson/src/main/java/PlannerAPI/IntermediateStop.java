package PlannerAPI;

public class IntermediateStop {
    private double lat;
    private double lon;
    private String orig;
    private String stopCode;
    private String vertexType;

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }

    public String getOrig() {
        return orig;
    }

    public String getStopCode() {
        return stopCode;
    }

    public String getVertexType() {
        return vertexType;
    }
}
